/**
 * This class is used to showcase how we declare variables in Java.
 */
public class Variables {
    
    public static void main(String[] args)
    {
        /*
         * The declaration of a variable looks as follows:
         * {data-type} {variable};
        */

        // This declares the variable "x" as an integer
        int x;

        // You can also assign a value of it at this point
        int y = 10;
    }
}